"""ABOUTME: Pre-defined fill pattern templates for Tambor drum machine.
ABOUTME: Contains 16 musically-interesting fill variations that can be assigned to patterns."""

def expand_fill_to_steps(fill_pattern: list, target_steps: int) -> list:
    """Expand a 16-step fill template to match the current pattern's step count.

    Args:
        fill_pattern: Fill pattern with 8 drums × 16 steps
        target_steps: Target step count (2-32)

    Returns:
        Expanded fill pattern with 8 drums × target_steps
    """
    if target_steps == 16:
        return fill_pattern  # No expansion needed

    target_steps = max(2, min(32, target_steps))  # Clamp to valid range
    expanded = []

    for drum_idx, drum_pattern in enumerate(fill_pattern):
        if target_steps <= 16:
            # Shrink: take first N steps
            expanded_drum = drum_pattern[:target_steps]
        else:
            # Expand: repeat pattern to fill target steps
            expanded_drum = []
            for step_idx in range(target_steps):
                # Map step to 16-step cycle (with repetition for 32 steps, etc.)
                source_step = (step_idx * 16) // target_steps
                source_step = min(source_step, 15)  # Clamp to 0-15
                expanded_drum.append(drum_pattern[source_step].copy())

        expanded.append(expanded_drum)

    return expanded


