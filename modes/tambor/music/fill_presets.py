def get_fill_template(fill_id: int) -> dict:
    """Get a fill template by ID."""
    return FILL_TEMPLATES.get(fill_id)


def get_all_fill_ids() -> list:
    """Get list of all available fill IDs."""
    return sorted(list(FILL_TEMPLATES.keys()))


def get_fill_names() -> dict:
    """Get mapping of fill IDs to names and descriptions."""
    return {
        fill_id: {
            "name": data["name"],
            "description": data["description"]
        }
        for fill_id, data in FILL_TEMPLATES.items()
    }
